public class Container {
    public int mass;
    public int height;
    public int tareWeight;
    public int weight;
    public int depth;
    public int maxPayload;

    public Container(int mass, int height, int tareWeight, int weight, int depth, int maxPayload) {
        this.mass = mass;
        this.height = height;
        this.tareWeight = tareWeight;
        this.weight = weight;
        this.depth = depth;
        this.maxPayload = maxPayload;

    }
}